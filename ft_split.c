#include "libft.h"

char **ft_split(char const *s, char c)
{
	int	i;
	int	j; // to calculate each string's length

	i = 0;
	j = 0;
	while(s[i])
	{
		if(s[i] == c){
			j = 0;
			
		}
		j++;
		i++;
	}
	
}
